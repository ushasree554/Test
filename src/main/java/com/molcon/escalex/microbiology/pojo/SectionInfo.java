package com.molcon.escalex.microbiology.pojo;

public class SectionInfo {

	
	private String id;
	private String section_title;
	private String sub_section_title;
	private String sub_section_text;
	private int sub_section_id;
	private String sub_section_display;
	
	private double sub_section_display_id;
	private int section_display_id;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSection_title() {
		return section_title;
	}
	public void setSection_title(String section_title) {
		this.section_title = section_title;
	}
	public String getSub_section_title() {
		return sub_section_title;
	}
	public void setSub_section_title(String sub_section_title) {
		this.sub_section_title = sub_section_title;
	}
	public String getSub_section_text() {
		return sub_section_text;
	}
	public void setSub_section_text(String sub_section_text) {
		this.sub_section_text = sub_section_text;
	}
	public int getSub_section_id() {
		return sub_section_id;
	}
	public void setSub_section_id(int sub_section_id) {
		this.sub_section_id = sub_section_id;
	}
	public String getSub_section_display() {
		return sub_section_display;
	}
	public void setSub_section_display(String sub_section_display) {
		this.sub_section_display = sub_section_display;
	}
	public double getSub_section_display_id() {
		return sub_section_display_id;
	}
	public void setSub_section_display_id(double sub_section_display_id) {
		this.sub_section_display_id = sub_section_display_id;
	}
	public int getSection_display_id() {
		return section_display_id;
	}
	public void setSection_display_id(int section_display_id) {
		this.section_display_id = section_display_id;
	}
	
	
	
	
}
